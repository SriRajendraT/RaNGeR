class WeekDaysUsing  {
	public static void main(String[] args) {
		String s="Tuesday";
		switch(s) {
		case "Sunday": {
		System.out.println(s+" is 1");
		break;
}
		case "Monday": {
		System.out.println(s+" is 2");
		break;
}
		case "Tuesday": {
		System.out.println(s+" is 3");
		break;
}
		case "Wednesday": {
		System.out.println(s+" is 4");
		break;
}
		case "Thursday": {
		System.out.println(s+" is 5");
		break;
}
		case "Friday": {
		System.out.println(s+" is 6");
		break;
}
		case "Saturday": {
		System.out.println(s+" is 7");
		break;
}
}	
}
}