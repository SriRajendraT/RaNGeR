class WeekDays {
	public static void main(String[] args) {
		int i=4;
		switch(i) {
		case 1: {
		System.out.println(i+" is Sunday");
		break;
}
		case 2: {
		System.out.println(i+" is Monday");
		break;
}
		case 3: {
		System.out.println(i+" is Tuesday");
		break;
}
		case 4: {
		System.out.println(i+" is Wednesday");
		break;
}
		case 5: {
		System.out.println(i+" is Thursday");
		break;
}
		case 6: {
		System.out.println(i+" is Friday");
		break;
}
		case 7: {
		System.out.println(i+" is Saturday");
		break;
}
}	
}
}